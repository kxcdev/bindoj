let gen_dep = "../gen.exe"

module Dparts = Dune_file_parts
include Dparts.Fcomb.Ops

module Dcomb = struct
  [@@@warning "-32"]

  include Dparts.Fcomb
  include Dparts
end

type target_kind = [
  | `output
  | `output_full_bridge_only
]

let classify_target : string -> target_kind option = function
  | "output" -> Some `output
  | "output_full_bridge_only" -> Some `output_full_bridge_only
  | _ -> None

let () =
  let outf fmt = printf fmt in
  let output_fmtd_newline fmtd = outf "%a@." fmtd () in
  let target = ArgOptions.(get_option_exn (StringOption "-target")) in
  let output_duneinc_header target_name =
     outf ";; generated by %s@\n" __FILE__;
     outf ";; file: %s@\n" target_name;
  in
  (String.chop_suffix ".gen" target
  >>? fun target_name ->
    String.chop_suffix "-dune.inc" target_name
    >? Dparts.mangle_library_name
    >>? fun subdir_name ->
      classify_target subdir_name
      >? fun kind -> (target_name, subdir_name, kind))
  |> function
  | Some (target_name, subdir_name, kind) ->
    output_duneinc_header target_name;
    output_fmtd_newline & Dcomb.(
      let caml_module =
        let outputs = [fmt "%s_jsoo_gen.ml"; fmt "%s_jsoo_gen.mli"; fmt "%s_jsoo_trans_gen.ml"; fmt "%s_jsoo_trans_gen.mli" ] in
        let args =
          match kind with
          | `output -> []
          | `output_full_bridge_only -> [ str "-full-bridge-only" ]
        in
        Rule.mkp_gen [
          targets outputs;
          Deps.(mk [named "generator" & str gen_dep]);
          (let single f =
            let open Action in
            with_stdout_to (`expr f) (
              run "%{generator}" & f :: args
            )
          in

          Action.mk_progn (outputs |&> single));
        ] in
      aggregate [
        subdir subdir_name [
          Library.mk []
            ~name:(str & sprintf "bindoj_objintf_gen_jsoo_test_gen_%s" subdir_name)
            ~modules':[ list ~sep:sp (fmt "%s_jsoo_gen"); list ~sep:sp (fmt "%s_jsoo_trans_gen") ]
            ~libraries:([
              "bindoj_objintf_gen_jsoo_test_gen_utils";
              "bindoj_test_common_typedesc_generated_examples";
              "bindoj.base";
              "bindoj.std_runtime";
              "bindoj.objintf_shared";
            ] @ ( match kind with
                | `output -> []
                | `output_full_bridge_only -> [ "bindoj_objintf_gen_test_gen_output" ]
              ) @ [
              "kxclib";
              "kxclib.js";
              "js_of_ocaml";
            ]);
            many caml_module
        ]
      ]
      |> const & (
        let open Bindoj_test_common_objintf_examples in
        All.all |&> (fun (module M: Utils.Ex) -> M.module_name |> String.lowercase_ascii))
    )
  | _ -> eprintf "invalid -target value %S" target; exit 2
